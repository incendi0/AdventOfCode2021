pub fn first_part(xs: &Vec<String>) -> i32 {
    let mut total = 0;
    for s in xs {
        let ss = s.split('|').collect::<Vec<&str>>();
        for output in ss[1].split_ascii_whitespace() {
            if output.len() == 2 || output.len() == 3 || output.len() == 4 || output.len() == 7 {
                total += 1;
            }
        }
    }
    total
}

